     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'tittle' => 'required',
            'code' => 'required|unique:coupons,code',
            'value' => 'required'
        ]);

        $newcoupan = new Coupon();
        $newcoupan->tittle = $request->post('tittle');
        $newcoupan->code = $request->post('code');
        $newcoupan->value = $request->post('value');
        $newcoupan->save();
        $save = $newcoupan->save();
        if ($save) {
            return redirect('admin/coupon')->with("success", "Category added successfully");
        } else {
            return redirect('admin/createCoupon')->with("faild", "Category added Faild");
        }
    }

