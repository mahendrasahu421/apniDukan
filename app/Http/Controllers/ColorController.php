    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'color_name' => 'required',
            'color_code' => 'required|unique:colors,color_code',
            'status' => 'required',
        ]);
        $color = new Color();
        $color->color_name = $request->post('color_name');
        $color->color_code = $request->post('color_code');
        $color->status = $request->post('status');
        $save = $color->save();
        if ($save) {
            return redirect('admin/color')->with("success", "Color added successfully");
        } else {
            return back()->withInput()->with("error", "Something went wrong!");
        }
    }

